// We create strings by using single quotes, double quotes or back ticks

'This is a string'

"This is also a string"

// Defining string with single or double quotes is the same in Javascript

// the line below won't work because Javascript will think the String has
// finished after the first second single quote
// 'he's tall'
"he's tall"
'he\'s tall'

// This is an ES6 feature
`This another way to write a string as well`
// the main advantage of using backticks is to utilize string interplation
`2 to the power of 8 is ${2 ** 8}`
